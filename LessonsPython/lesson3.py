# Напишите программу, которая запрашивает ввод двух значений. Если хотя
# бы одно из них не является числом, то должна выполняться конкатенация,
# т. е. соединение, строк. В остальных случаях введенные числа
# суммируются.
try:
    value1 = input("Первое значение: ")
    value2 = input("Второе значение: ")
    value1 = int(value1)
    value2 = int(value2)
except ValueError:
    print(str(value1) + value2)
else:
    print("Результат: ", value1 + value2)


